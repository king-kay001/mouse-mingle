function smarty_function_mekirim_question_icon( $params )
{
    if ( !isset($params['name']) )
    {
        throw new InvalidArgumentException('Empty input name!');
    }

    $questionIcon = MOUSE_BOL_Service::getInstance()->getProfileQuestionIcon($params['name']);

    return UTIL_HtmlTag::generateTag('span', [
        'class' => $questionIcon ? $questionIcon : 'ow_ic_info'
    ], true);
}

function smarty_function_mekirim_menu_bubble( $params )
{
    if ( !isset($params['key']) )
    {
        throw new InvalidArgumentException('Empty input key!');
    }

    $userId = OW::getUser()->getId();
    $showBubble = false;

    switch( $params['key'] )
    {
        case 'notification':
            $showBubble =  (int) NOTIFICATIONS_BOL_NotificationDao::getInstance()
                ->findNotificationCount($userId, 0);
        break;
        case 'message':
            $showBubble =  (int) MAILBOX_BOL_ConversationService::getInstance()
                ->getUnreadMessageCount($userId);
        break;
    }

    return $showBubble ? '<span class="menu_bubble"></span>' : null;
}

function smarty_function_mekirim_question_value( $params )
{
    if ( !isset($params['name']) )
    {
        throw new InvalidArgumentException('Empty input name!');
    }

    // removed hardcoded userId and pass paramater on template
    $params['userId'] = OW::getUser()->getId();

    if ( !isset($params['userId']) )
    {
        throw new InvalidArgumentException('Empty input userId!');
    }

    if ( !isset($params['type']) )
    {
        throw new InvalidArgumentException('Empty input type!');
    }

    if ( empty($value = MOUSE_BOL_UserService::getInstance()->getFieldValue( $params['userId'], $params['name']) ) || !isset($value[$params['name']]) )
    {
        return null;
    }

    $typeList = [
        BOL_QuestionService::QUESTION_PRESENTATION_RADIO,
        BOL_QuestionService::QUESTION_PRESENTATION_CHECKBOX,
        BOL_QuestionService::QUESTION_PRESENTATION_SELECT,
        BOL_QuestionService::QUESTION_PRESENTATION_FSELECT,
        BOL_QuestionService::QUESTION_PRESENTATION_MULTICHECKBOX,
    ];

    $isIntType = in_array($params['type'], $typeList);

    $value = BOL_QuestionService::getInstance()->prepareFieldValue($params['type'], $value[$params['name']]);

    if( is_array($value) )
    {
        $result = [];

        foreach( $value as $v )
        {
            $result[] = MOUSE_BOL_UserService::getInstance()->getFieldValueLabel($params['name'], $v);
        }

        return implode(", ", $result);
    }
    else
    {
        if ( $isIntType )
        {
            return MOUSE_BOL_UserService::getInstance()->getFieldValueLabel($params['name'], $value); 
        }
    }

    return $value;
}

function smarty_function_mekirim_format_price( $params, $smarty )
{
    if ( !isset($params['price']) )
    {
        throw new InvalidArgumentException('Empty input price!');
    }

    return OW::getLanguage()->text('mouse', 'plan_struct', array(
        'price' => floatval( $params['price'] ),
        'currency' => BOL_BillingService::getInstance()->getActiveCurrency()
    ));
}

function smarty_function_mekirim_features( $params, $smarty )
{
    if ( !isset($params['data']) )
    {
        throw new InvalidArgumentException('Empty input data!');
    }

    $glue = isset($params['glue']) ? $params['glue'] : ", ";

    return implode($glue, $params['data']);
}

